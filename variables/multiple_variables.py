#variable assignment can be done in a single line

a, b, c = "first", "second", "third"
print(a)
print(b)
print(c)

# If multiple variables, have same values then do below

x = y = z = "Trinity"
print(x)
print(y)
print(z)

#You can use UNPACKING to assign variables too
var1, var2, var3 = ["Emeka", "Tommyzan", "Wisdom"]
print(var1, var2, var3)